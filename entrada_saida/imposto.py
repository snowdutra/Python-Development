valor = float(input("Digite aqui o seu salário: "))

if valor == 2000:
    print("Isento")
    imposto = 0
    
    
elif valor == 3000:
    imposto = (valor - 2000) * 8 / 100

elif valor <= 4500:
    imposto = 8 / 100 * 1000 + (valor - 3000) * 18 / 100

else:
    imposto = 8 / 100 * 1000 + 18 / 100 * 1000 + (valor - 4500) * 28 / 100

print(f"O valor do imposto de renda é: ${imposto:.2f}")
